const VendorDropdown = ({ value, onChange, onPayeeCodeChange, showClear }) => {
  const handleVendorChange = (e) => {
    const selectedVendor = e.target.value;
    onChange(selectedVendor);
    
    // Auto-populate payee code if mapping exists
    const payeeCode = vendorPayeeCodes[selectedVendor] || '';
    onPayeeCodeChange(payeeCode);
  };

  const handleClear = () => {
    onChange('');
    onPayeeCodeChange('');
  };

  return (
    <DropdownContainer>
      <Select value={value} onChange={handleVendorChange}>
        <option value="">Select Vendor</option>
        {vendors.map((vendor) => (
          <option key={vendor} value={vendor}>
            {vendor}
          </option>
        ))}
      </Select>
      {showClear && value && (
        <ClearButton type="button" onClick={handleClear}>
          <X size={12} />
        </ClearButton>
      )}
    </DropdownContainer>
  );
};

